<section class="rate-factors">
    <div class="container border-top">
        
        <div class="text-center">
            <h2>Factors That Affect the Cost to Ship Your Car</h2>
            <p class="lead text-muted">Rate Variables Explained</p>
        </div>
        
        <div class="row">
            <div class="col-md-6">
                <div class="factor factor-distance">
                    <h3>Distance</h3>
                    <p class="lead">Mileage and Range</p>
                    <hr>
                    <p>Your price is calculated by mileage relative to range (long or short). Longer distances have a lower per-mile cost to transport a car.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>Shorter distances mean more of the trucker’s time is spent picking up the load instead hauling the load, so that increases the cost of your delivery.</li>
                        <li>Simply put, truckers make their money on the road.</li>
                    </ul>
                </div>
            </div>
            
            <div class="col-md-6">
                <div class="factor factor-population">
                    <h3>Population</h3>
                    <p class="lead">Metro or Rural</p>
                    <hr>
                    <p>This impacts the number of truckers traveling to a specific location. Transport to less populated areas will increase the price to ship a car.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>On average, higher-populated areas have more truckers driving through to transport vehicles.</li>
                        <li>Requesting a Pick up or a Drop off in a rural area will affect your calculated quote because truckers will have to veer away from our most popular routes.</li>
                    </ul>
                </div>
            </div>
            
            <div class="col-md-6">
                <div class="factor factor-condition">
                    <h3>Condition</h3>
                    <p class="lead">Operable or Inoperable</p>
                    <hr>
                    <p>Transporting non-drivable vehicles increases your rate.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>A drivable car is easily driven onto the carrier trailer.</li>
                        <li>Non-operable vehicles are much harder to load, and they should still roll, brake, and steer.</li>
                        <li>Additional accommodation is necessary for cars that do not roll.</li>
                    </ul>
                </div>
            </div>
            
            <div class="col-md-6">
                <div class="factor factor-make-model">
                    <h3>Make & Model</h3>
                    <p class="lead">Weight, Space, and Handling</p>
                    <hr>
                    <p>Determined by car Type, Size, and Model.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>Truckers have to account for the total weight they can haul and the space available on their truck.</li>
                        <li>Longer and/or taller vehicles must be properly accommodated for in the transport trailer.</li>
                    </ul>
                </div>
            </div>
            
            <div class="col-md-6">
                <div class="factor factor-transport">
                    <h3>Transport Type</h3>
                    <p class="lead">Open or Enclosed</p>
                    <hr>
                    <p>Open Transport is standard and does not affect the price to ship the car. Enclosed Transport is a minimum 25% higher rate.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>Enclosed Transport provides an extra level of protection for vehicles that need special care.</li>
                        <li>This service is recommended for cars worth $50,000+, classic cars, or collector cars that are not regularly driven.</li>
                    </ul>
                </div>
            </div>
            
            <div class="col-md-6">
                <div class="factor factor-seasonality">
                    <h3>Seasonality</h3>
                    <p class="lead">Route Popularity</p>
                    <hr>
                    <p>Market and seasonal demands affect route popularity and car shipping costs.</p>
                    
                    <ul class="list-unstyled list-chevron">
                        <li>The market supply and demand fluctuates along different routes based on seasonality and location.</li>
                        <li>The direction of travel will also affect your calculated quote, as market supply and demand can be higher or lower along one way of a route as opposed to the other.</li>
                    </ul>
                </div>
            </div>
        </div>
        
    </div>
</section>      <!-- .rate-factors -->





